-- Выпуск подарочной карты — это выпуск денег, и до сих пор он был единственным
-- денежным путём без защиты от повтора. Проверка по `code` реплей не ловила:
-- когда код не передан, сервер генерирует новый на каждый вызов, поэтому второй
-- одинаковый запрос создавал вторую карту с полным балансом и вторую проводку
-- в 2300. Обе внутренне корректны, расхождения не видел ни один риск-сигнал.
--
-- Колонка nullable: исторические карты ключа не имеют, и требовать его задним
-- числом нельзя. Уникальность частичная — NULL в Postgres не конфликтуют между
-- собой, так что старые строки друг другу не мешают.
ALTER TABLE "GiftCard" ADD COLUMN "idempotencyKey" TEXT;

CREATE UNIQUE INDEX "GiftCard_idempotencyKey_key" ON "GiftCard"("idempotencyKey");
